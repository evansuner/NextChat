import EmojiPicker, {
  EmojiStyle,
  Theme as EmojiTheme,
} from "emoji-picker-react";
import { getEmojiUrl } from "./emoji";

export function AvatarPicker(props: {
  onEmojiClick: (emojiId: string) => void;
}) {
  return (
    <EmojiPicker
      width={"100%"}
      lazyLoadEmojis
      theme={EmojiTheme.AUTO}
      getEmojiUrl={getEmojiUrl}
      onEmojiClick={(e) => {
        props.onEmojiClick(e.unified);
      }}
    />
  );
}
